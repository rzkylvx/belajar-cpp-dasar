#include <iostream>

/**
 * @brief Entry point of the program.
 *
 * Initializes an integer variable 'a' with the value 7,
 * then prints its value to the standard output followed by a newline.
 *
 * @return int Returns 0 upon successful execution.
 */
int main() {
  int a = 7;

  std::cout << a << std::endl;
  return 0;
}